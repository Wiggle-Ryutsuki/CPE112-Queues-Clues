// Random cat generator
void printRandomCat(){
    srand(time(NULL));

    // Generate number from 1 to 3
    int number = rand() % 3 + 1;

    // Random cats
    switch (number) {
        case 1:
            printCutecat();
            break;
        case 2:
            printGrumpycat();
            break;
        case 3:
            printDumbcat();
            break;
        default:
            printf("An error has occured when generating random cat.\n");
    }
}